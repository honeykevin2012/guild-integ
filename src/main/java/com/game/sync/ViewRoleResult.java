public class ViewRoleResult {
	private String serverId;
	private String serverName;
	private String roleId;
	private String roleName;
	private String gameAmount;
	private String platAmount;

	public String getServerId() {
		return serverId;
	}

	public void setServerId(String serverId) {
		this.serverId = serverId;
	}

	public String getServerName() {
		return serverName;
	}

	public void setServerName(String serverName) {
		this.serverName = serverName;
	}

	public String getRoleId() {
		return roleId;
	}

	public void setRoleId(String roleId) {
		this.roleId = roleId;
	}

	public String getRoleName() {
		return roleName;
	}

	public void setRoleName(String roleName) {
		this.roleName = roleName;
	}

	public String getGameAmount() {
		return gameAmount;
	}

	public void setGameAmount(String gameAmount) {
		this.gameAmount = gameAmount;
	}

	public String getPlatAmount() {
		return platAmount;
	}

	public void setPlatAmount(String platAmount) {
		this.platAmount = platAmount;
	}
}
